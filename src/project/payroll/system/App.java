    private void btnprintActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnprintActionPerformed
        // TODO add your handling code here:
        try{
            String selected = cb_projectname.getItemAt(cb_projectname.getSelectedIndex());
            conn = getConnection(PG.url, PG.username, PG.password);
            JasperDesign jd = JRXmlLoader.load("src/project/payroll/system/payroll.jrxml");
            java.sql.Date dstart = new java.sql.Date(dps.getDate().getTime());
            java.sql.Date dend = new java.sql.Date(dpe.getDate().getTime());
//            String where = "datestarted >= '"+dstart+"' AND dateended <= '"+dend+"' AND projectname='"+pname+"'";
//            String sql = "SELECT *, "
//                    +"(SELECT SUM(salary) FROM payroll WHERE "+where+" ) AS totalSalary"
//                    + "FROM payroll";
            String sql = "select *,\n" +
                    "(SELECT SUM(salary) from payroll WHERE datestarted >= '"+dstart+"' AND dateended <= '"+dend+"' AND projectname = '"+selected+"') AS totalSalary \n" +
                    "from payroll\n" +
                    "WHERE datestarted >= '"+dstart+"' AND dateended <= '"+dend+"' AND projectname = '"+selected+"'";
            JRDesignQuery newQuery = new JRDesignQuery();
            newQuery.setText(sql);
            jd.setQuery(newQuery);
            
            JasperReport jr = JasperCompileManager.compileReport(jd);
            JasperPrint jp = JasperFillManager.fillReport(jr, null, conn);
            JasperViewer.viewReport(jp, false);
            
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, "Print erro: "+e);
        }
    }//GEN-LAST:event_btnprintActionPerformed

